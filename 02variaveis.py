# 1. Variaáveis

idade = 26 # Criando uma variável
print(idade)

nome = 'Paulo Toledo de Barros'
print(nome)

"""
    Tipos de Variaáveis
    1. int: números inteiros, ou seja números sem partes decimal: 0, 5, -1, 10000
    2. float: números reais, ou seja, números com parte decimal: 1.0, -2.7, 3.14
    3. str: cadeias de caracteres, ou seja, dados textuais.
    4. bool: valores lógicos (booleanos): True ou False.
"""

idade = 26
altura = 1.77
nome = 'Paulo Barros'
estudando = True

print(type(idade) )
print(type(altura) )
print(type(nome) )
print(type(estudando) )

